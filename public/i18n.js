function createLanguageToggle() {
  if (document.querySelector('.language-toggle')) return;
  const button = document.createElement('button');
  button.className = 'language-toggle';
  button.type = 'button';
  button.textContent = localStorage.getItem('site-lang') === 'en' ? '繁中' : 'EN';
  button.addEventListener('click', () => {
    const next = document.documentElement.dataset.lang === 'en' ? 'zh' : 'en';
    setLanguage(next);
  });
  document.body.appendChild(button);
}

function translateText(text, lang) {
  if (lang === 'zh') return text;
  let result = I18N[text] || text;
  for (const [pattern, replacement] of REPLACEMENTS) {
    result = result.replace(pattern, replacement);
  }
  return result;
}

function shouldSkipNode(node) {
  const parent = node.parentElement;
  if (!parent) return true;
  return ['SCRIPT', 'STYLE', 'TEXTAREA', 'CODE', 'PRE'].includes(parent.tagName);
}

function walkTextNodes(root, callback) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (shouldSkipNode(node)) return NodeFilter.FILTER_REJECT;
      if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(callback);
}

function setLanguage(lang) {
  document.documentElement.dataset.lang = lang;
  document.documentElement.lang = lang === 'en' ? 'en' : 'zh-Hant';
  localStorage.setItem('site-lang', lang);
  walkTextNodes(document.body, (node) => {
    if (!node.datasetOriginal) node.datasetOriginal = node.nodeValue;
    node.nodeValue = lang === 'en' ? translateText(node.datasetOriginal, 'en') : node.datasetOriginal;
  });
  const button = document.querySelector('.language-toggle');
  if (button) button.textContent = lang === 'en' ? '繁中' : 'EN';
}

function initI18n() {
  createLanguageToggle();
  setLanguage(localStorage.getItem('site-lang') || 'zh');
}

setTimeout(initI18n, 300);
